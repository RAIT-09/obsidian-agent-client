import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import { copyFileSync, existsSync } from "fs";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.argv[2] === "production";

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins,
	],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
	minify: prod,
});

// Copy pty-helper.py to dist
const srcHelper = "src/infrastructure/pty/pty-helper.py";
const destHelper = "pty-helper.py";

if (existsSync(srcHelper)) {
	copyFileSync(srcHelper, destHelper);
	console.log("Copied pty-helper.py");
}

// Copy xterm.css to dist
const srcXtermCss = "node_modules/xterm/css/xterm.css";
const destXtermCss = "xterm.css";

if (existsSync(srcXtermCss)) {
	copyFileSync(srcXtermCss, destXtermCss);
	console.log("Copied xterm.css");
} else {
	console.error("Could not find xterm.css in node_modules");
}

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
